            <p class="justify">Many of the natural springs in the Himalayas are drying up, many perennial springs which are the only source of drinking water for its people have now become seasonal. Tourism is only aggravating this stress on land and water with the locals having to suffer. Water bodies like Indus and Pangong lake in Ladakh are bearing the brunt of tourism after movies like Three Idiots hit the theatre. The least polluted parts of the country are now swarmed with tourists and the waste that accompanies them. <br><br>
But does that mean that one stops travelling? Of course, not! As the saying goes- “<i>if we were meant to stay in one place, we would have roots instead of feet</i>”. But clearly there is something wrong in the way we are doing it. In our drive to feed our wanderlust, we are only becoming inconsiderate towards our environment. <br><br>
We are forgetting that these destinations are living eco-systems. We are forgetting that places are not just lands meant for outsiders to revitalize. They are home to people who have been living in these environments much before these places were put on the map or stirred any social media buzz. We forget that they are also home to the flora and fauna that make these destinations so rich and distinct. <br><br>
</p>
            
            <img src="/images/tent.jpg" alt="Eco Tourism" class="img-fluid"><br><br>
            
            <p class="justify">We love travelling as much as you do, but we, at Encamp Adventures have pledged ourselves to do it more responsibly this time. In this pursuit, we have planned to cater services that are zero-waste in nature (for nature!).  Our “Zero-Waste Campsite” has been carefully designed keeping in mind all that has been said above. We are trying to create a group of conscious and responsible travellers, travellers with not just memorable take-aways but also the ones who give back in their own little mindful ways.<br><br>
Not only will you be staying amidst nature; you will be doing so without the guilt of harming the environment. We adopt responsible measures in cutting down on plastic waste, utilizing composting bins, using eco-friendly and reusable products instead of disposable plastic, and many more. All you got to do is join us in our endeavour in making camping more fun and environment friendly. There’s so much we can do, if we come together and care for our nature. So, come join us. Travel light and travel right with Encamp Adventures!<br><br>
