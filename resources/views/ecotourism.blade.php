            <p class="justify">Have you been planning backpacking with your best-friend for the longest time?  Are you also yearning to retreat into lush green landscapes, away from the city’s hustle bustle? Do you plan to use your hard earned leaves to visit your dream destinations this year?<br><br>
If this resonates with you, we are sure you’ve been bitten by the travel bug too.  Travelling is all about learning and unlearning on the move. <i>Traversing places different than our daily life idyll. Seeking refuge in spaces that evoke a sense of comfort away from home. Immersing in different cultures and appreciating the diversities</i>. Travelling indeed can be a life enriching adventure.<br><br>
Studies around the world speak of the health benefits of spending time outside in nature, green spaces, especially forests. It helps us to de-stress and take a much deserved break from the everyday monotony of life.  But as much as we want to explore places, we often turn a blind eye to the environment in which are placing ourselves.<br><br>
Globally, of all the solid waste generated, 14 per cent is produced solely by tourists every year. This share amounts to 4.8 million tonnes. If numbers put you off, there are some examples for which we don’t have to go too far. <i>Manali</i> fondly known as the backpacking capital has turned into a dump yard. This year 2,000 tonnes of waste was left behind by tourists during the peak holiday season. Everyday, tourists were producing thrice of what is generally produced as garbage on any other day in the town.<br><br>
</p>
            
