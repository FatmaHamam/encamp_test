            <h4>How do we reach this Paradise of a place?</h4><br>
            <img src="/images/reachdz.jpg" alt="Dzukou trek" class="img-fluid"><br><br>
            <p class="justify">Between the borders of Nagaland and Manipur, lies the breathtaking Dzukou Valley. The two common routes to reach Dzukou are the Viswema route and the Jakhama route in Nagaland. Although the route taken via Jakhama is relatively shorter, it is a steep climb and prone to slippery stones during monsoons.<br><br>
We recommend starting the trek via Viswema route, which can be reached by a cab from Dimapur Railway Station. The time taken may be longer but is comparatively easy to trek. Vehicles drop you next to the Guest House which is around 8kms trek from the ticket booth/entry gate for Dzukou from Viswema. So, 8kms of the trek can be cut short, whereas in Jakhama you begin your trek right from the main road.<br><br>
You’ll reach the base camp first – from where you begin the trek; one of the best in your lifetime.<br><br>
<img src="/images/basecamp.jpg" alt="Base camp at Dzukou Valley" class="img-fluid"><br><br>
There is a helipad near the base camp. But since it is not operational always, it is also used as a camping ground by campers.<br><br><br>
</p>
            <h4>Few things to know before your Dzukou Trek</h4><br><br>
            <img src="/images/fewthings.jpg" alt="Dzukou trek" class="img-fluid"><br><br>
            <h5>Prior physical activity advised </h5>
            
            <p class="justify">Like we have mentioned above, the trek isn’t an easy one. If you’re someone who indulge in little to no physical activity in your everyday routine, it will be difficult to complete the trek. Because, the route via Viswema, although easier, still requires a steep climb of 1 to 1.5 hours. <a href="http://www.encampadventures.com/contact">For more details on the trek, feel free to contact us here.</a><br><br>
Bonus fact: While going from Viswema, after the steep trek, there is a way which leads to Mt Temfu.
</p><br>

            <h5>Documentation that you need</h5>
            <p class="justify">Inner Line permits (ILP) are required to enter Nagaland. If you’re travelling with us on this trip to Dzukou, your ILPs will be arranged from our end. 
</p>
<br>
            <h5>Ticket Charges</h5>
            <p class="justify">Rs 20 for the locals, Rs 50 for Locals (from Nagaland) Rs 100 for Indians, Rs 200 for Foreign tourist and Rs 200 for a camera.
            </p><br>
            
            <h5>Plastic-free zone</h5><br>
            <img src="/images/plasticfree.jpg" alt="Dzukou trek" class="img-fluid"><br><br>
            <p class="justify">In case you weren’t aware, Dzukou Valley has been declared a plastic-free zone. Although you’re allowed to carry plastic items along, you can’t leave any plastic trash behind. As a matter of fact, before beginning the trek, all plastic items are counted and tallied against the number of items you carry back on your return from the valley. Penalty is levied upon violation of these norms.
            </p><br>
            
            <h5>Tobacco free zone</h5>
            <p class="justify">The tranquil nature of the valley demands not only your appreciation, but your respect as well. Tobacco is strictly prohibited in the valley, and all those who venture on to this rewarding journey respect the norms and leave behind their cigarettes. Trust us on this – it’s worth every rule followed in the valley.
            </p><br>
            
            <h5>No Drones without prior consent</h5>
            <p class="justify">The Dzukou Valley is managed by an entity called SAYO. It is mandatory to take prior approval from SAYO before flying drones in the valley. Drones are prohibited, otherwise.
            </p><br><br>
            
             <h5>Preparing for the trek</h5><br>
             <img src="/images/preparingtrek.jpg" alt="Dzukou trek" class="img-fluid"><br><br>
            <p class="justify">Just a few small pointers to prepare better for your trek to Dzukou Valley –
            </p>
            <p class="justify"><b>Raincoats & trekking shoes</b> – Rainfalls are common in the area, and the trek could be a slippery one. It is always recommended to gear up with a pair of trusty trekking shoes.
            </p>
            <p class="justify"><b>Full Pants are ideal </b>– The urge is real to wear a pair of half pants or shorts. But there are certain plants in the valley that could cause immense itching if your legs come in contact.
            </p>
            <p class="justify"><b>Carry a reusable water bottle</b> – Plastics can’t be left behind in the valley, and the best way to avoid purchasing water on your way is to carry one from home. You can fill it up on your stops. </p>
            <p class="justify"><b>Pack dry fruits and fresh fruits on the go</b> – You need your energy for the trek in its fullest; a clean & healthy diet is the way to go. Even up in the valley, food prepared is simple, yet delicious.
            </p>
            <p class="justify"><b>Glucose D</b> – The energy drink for campions, we say! In between your trek, you would need refreshments to keep going. A little energy booster work wonders here.
            </p><br><br>
            
            
